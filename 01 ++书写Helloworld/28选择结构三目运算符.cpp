int main28() {
	//a和b比较，哪个大将其赋值给c
	int a = 10;
	int b = 20;
	int c = 0;

	c = a > b ? a : b; // 如果a > b为真，返回a，；如果a > b为假，返回b。
	cout << "c = " << c << endl;

	//C++中三目运算符返回的是变量,可以继续赋值

	(a > b ? a : b) = 100; //将返回的那个值赋值为100，b大，将b赋值为100

	cout << "a = " << a << endl;
	cout << "b = " << b << endl;

	return 0;
}