#include<iostream>
using namespace std;


//三目运算符：表达式1 ? 表达式2 ：表达式3;
//如果表达式1的值为真，执行表达式2，并返回表达式2的结果；如果表达式1的值为假，执行表达式3，并返回表达式3的结果。
//和if语句比较，三目运算符优点是短小整洁，缺点是如果用嵌套，结构不清晰

